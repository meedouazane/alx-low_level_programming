#include <stdio.h>
/**
 * add - adding to int
 * @a: first int
 * @b: second int
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub between two int
 * @a: first int
 * @b: second int
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - mul of two int
 * @a: first int
 * @b: second int
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - div of two int
 * @a: first int
 * @b: second int
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
 /**
 * mod - mod of two int
 * @a: first int
 * @b: second int
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
